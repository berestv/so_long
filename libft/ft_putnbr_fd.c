#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	aux;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	if (n < 0 && n != -2147483648)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		n %= 10;
	}
	if (n < 10 && n >= 0)
	{
		aux = n + 48;
		write(fd, &aux, 1);
	}
}
